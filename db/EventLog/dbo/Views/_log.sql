CREATE VIEW dbo._log
AS
SELECT          dbo.Logs.sn, dbo.Logs.ProcessId, dbo.Process.Name AS ProcessName, dbo.Logs.MessageId, dbo.Logs.Time, 
                            dbo.SqlLogs.ExecuteTime, dbo.SqlLogs.DataSource, dbo.SqlLogs.CommandText, dbo.Logs.Category, 
                            dbo.Logs.LogLevel, dbo.Logs.EventId, dbo.Logs.EventName, dbo.Logs.Message, dbo.Logs.Exception
FROM              dbo.Logs INNER JOIN
                            dbo.Process ON dbo.Logs.ProcessId = dbo.Process.Id LEFT OUTER JOIN
                            dbo.SqlLogs ON dbo.Logs.sn = dbo.SqlLogs.sn
GO
